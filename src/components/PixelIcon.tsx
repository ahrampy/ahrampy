export const PixelIcon = ({
	sprite,
	size = 3,
	className = '',
}: {
	sprite: keyof typeof SPRITES;
	size?: number;
	className?: string;
}) => {
	const data = SPRITES[sprite];

	const shadow = useMemo(() => {
		if (!data) return '';
		return data.pixels
			.map(([x, y]) => `${x * size}px ${y * size}px currentColor`)
			.join(', ');
	}, [sprite, size]);

	if (!data) return null;

	return (
		<span
			className={`inline-block text-accent ${className}`}
			style={{
				width: `${size}px`,
				height: `${size}px`,
				boxShadow: shadow,
				marginRight: `${(data.w - 1) * size}px`,
				marginBottom: `${(data.h - 1) * size}px`,
			}}
		/>
	);
};
